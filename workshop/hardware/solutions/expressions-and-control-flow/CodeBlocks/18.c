#include <stdio.h>
#include <stdint.h>

//ws-18

int main() {
    uint16_t number = 254;
    // if number can't be represented with 8bits
    // print 'longer than 8bits' otherwise print 'ok'
    if((uint8_t)number == (uint16_t)number) {
        printf("ok");
    } else {
        printf("longer than 8bits");
    }
    return 0;
}
